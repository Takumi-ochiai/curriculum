public class Example {
  public static void main(String[] args) {
    System.out.println("Hello World");
  }
}

// 「public class クラス名 {}」でクラスを定義
// Exampleクラスの中にmainメソッド「public static void main(String[])」を定義
// Exampleクラスの処理はprintlnメソッド「System.out.println()」を実行すると読むことができる
// ()内を引数と言い、"Hello World"を指定しているのでHello Worldが表示される
// Javaでは処理の末尾に;を付与することがルール

// 上記のファイルを「ソースファイル」と言い、ファイル名は「クラス名.java」になる
// ソースファイルが出来たらコンパイルする
// コンパイルとは人間が理解できるソースファイルをコンピューターが理解できる機械語に翻訳する処理のこと