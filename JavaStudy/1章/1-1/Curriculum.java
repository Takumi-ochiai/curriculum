// 例題1
public class Curriculum {
  public static void main(String[] args) {
    // int型を宣言して、「seisu」という変数名をつけて、1234で初期化する
    int seisu = 1234;
    System.out.println("int型 = 変数 = " + seisu);
  }
}


// 「System.out.println()」と「System.out.print()」の違い
// 「System.out.println()」は出力内容の末尾に改行が入る
// 「System.out.print()」は改行されない

// 改行文字は
// windowsは¥n
// Macは\n (\はoption+¥)


// 例題2
public class Curriculum2 {
  public static void main(String[] args) {
    // Stringクラスを宣言して、変数名はmojiで「javaはプログラム言語」で初期化
    String moji = "javaはプログラム言語";
    System.out.println(moji);
  }
}

